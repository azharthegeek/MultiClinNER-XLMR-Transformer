def get_zf():
    if not hasattr(thread_local, 'zf'):
        thread_local.zf = zipfile.ZipFile(zip_path, 'r')
    return thread_local.zf

def extract_batch(batch):
    zf = get_zf()
    for member in batch:
        zf.extract(member, dataset_output_dir)
    return len(batch)

