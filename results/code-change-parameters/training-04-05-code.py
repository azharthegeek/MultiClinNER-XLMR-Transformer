def predict(sentence, model, device):
    # Tokenize the sentence
    inputs = tokenizer(sentence, return_tensors='pt', truncation=True, padding=True).to(device)

    # Make predictions
    with torch.no_grad():
        outputs = model(**inputs)

    # Get predicted labels
    logits = outputs.logits
    predictions = torch.argmax(logits, dim=2).squeeze().cpu().numpy()

    # Align predictions with original tokens, skipping special tokens
    tokens = tokenizer.convert_ids_to_tokens(inputs['input_ids'].squeeze())
    true_predictions = [id2label[p] for p in predictions if p != -100]
    # Adjust tokens to match the length of true_predictions if special tokens were removed
    # This part might need refinement based on exact tokenizer behavior and label alignment in `id2label`
    # For a simple demo, we'll try to align one-to-one after removing special tokens and padding markers
    processed_tokens = []
    processed_preds = []
    for i, token_id in enumerate(inputs['input_ids'].squeeze()):
        if token_id not in tokenizer.all_special_ids and predictions[i] != -100:
            processed_tokens.append(tokenizer.decode(token_id))
            processed_preds.append(id2label[predictions[i]])
    return processed_tokens, processed_preds

