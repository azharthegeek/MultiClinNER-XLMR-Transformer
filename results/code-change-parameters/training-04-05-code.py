def extract_spans(sentence):
    """Return a list of (sentence, span_text, entity_type) tuples."""
    tokens, preds = predict(sentence, model, device)
    spans = []
    current_span_tokens = []
    current_span_type = None

    for token, pred_label in zip(tokens, preds):
        if pred_label.startswith("B-"):
            if current_span_tokens:
                spans.append((sentence, " ".join(current_span_tokens), current_span_type))
            current_span_tokens = [token]
            current_span_type = pred_label[2:]
        elif pred_label.startswith("I-") and current_span_tokens and current_span_type == pred_label[2:]:
            current_span_tokens.append(token)
        else:
            if current_span_tokens:
                spans.append((sentence, " ".join(current_span_tokens), current_span_type))
            current_span_tokens = []
            current_span_type = None

    if current_span_tokens:
        spans.append((sentence, " ".join(current_span_tokens), current_span_type))
    return spans


